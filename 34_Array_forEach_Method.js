let nums = [34, 89, 56, 54];

/**
 * forEach() is a method of an array
 * which fetch one value at a time and assigned to argument variable "n"
 * later "n" value passed on function with whom you can do anything
 */
nums.forEach(
    (n) => {
        console.log(n);
    }
)

